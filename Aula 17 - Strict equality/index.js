//   = assigmennt operator
//  == comparison operator (compare if values are equal)
// === strict equality operator (compare if values & datatype are equal)
//  != inequality operator
// !== strict inequality operator

const PI = 3.14;

if(PI == "3.14"){
    console.log ("That is Pi")
} else {
    console.log("This is not PI")
} // true

if(PI === "3.14"){
    console.log ("That is Pi")
} else {
    console.log("This is not PI")
} //false

if(PI === 3.14){
    console.log ("That is Pi")
} else {
    console.log("This is not PI")
} // true

if(PI != "3.14"){
    console.log ("That is Pi")
} else {
    console.log("This is not PI")
}// false

if(PI !== "3.14"){
    console.log ("That is Pi")
} else {
    console.log("This is not PI")
} // true